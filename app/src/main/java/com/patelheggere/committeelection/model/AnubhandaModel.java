package com.patelheggere.committeelection.model;

public class AnubhandaModel {
    private String name;
    private String phone;
    private String id;

    public AnubhandaModel(){}

    public AnubhandaModel(String name, String phone, String id) {
        this.name = name;
        this.phone = phone;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
